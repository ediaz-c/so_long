#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;

	i = 1;
	if (!lst)
		return (0);
	if (lst->next == NULL)
		return (1);
	while (lst->next)
	{
		lst = lst->next;
		i++;
	}
	return (i);
}
