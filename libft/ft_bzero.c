#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*reg;
	size_t			i;

	reg = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		reg[i] = '\0';
		i++;
	}
}
